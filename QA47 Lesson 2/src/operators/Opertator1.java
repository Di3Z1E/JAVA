package operators;

public class Opertator1 {

	public static void main(String[] args) {

		long num = 0;
		num = 20 + 30;
		System.out.println(num);
		
		num = 30 * 3 + 5;
		System.out.println(num);

		num = (10 + 5) * 3;
		System.out.println(num);
	
		num = num + 10;
		System.out.println(num);
		
		num++;
		System.out.println(num);

		
		num--;
		System.out.println(num);

		
		num = 10 % 3;
		System.out.println(num);
		
		num = 10 % 2;
		System.out.println(num);
		
		int a = 15;
		int b = 20;
		int c = a + b;
		System.out.println(c);
		
		}
}